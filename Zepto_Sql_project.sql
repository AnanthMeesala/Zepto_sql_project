-- PRODUCT NAMES PRESENT MULTIPLE TIMES
SELECT NAME,COUNT(*) AS "NUMBER OF SKUs"
FROM ZEPTO 
GROUP BY name 
HAVING COUNT(*)>1
ORDER BY COUNT(*) DESC;

